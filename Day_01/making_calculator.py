a = int(input("Enter the first number:"))
b = int(input("Enter the second number:"))

# very basic calculators for doing computations of two numbers


print("The value of addition is:",a+b)
print("The value of subtraction is:",a-b)
print("The value of multiplication is:",a*b)
print("The value of division is:",a/b)
print("The value of modulus is:",a%b)
print("The value of floor division is:",a//b)

# Division (/): Returns a floating-point result (with decimal values).
# Modulus (%): Returns the remainder of the division.
# Floor Division (//): Returns the quotient, rounded down to the nearest integer.
